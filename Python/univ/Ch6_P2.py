numA = int(input("첫 번째 수: "))
numB = int(input("두 번째 수: "))

print() # 공백 줄
print(str(numA) + "와 " + str(numB) + "는 같은가?", numA == numB)
print(str(numA) + "와 " + str(numB) + "가 다른가?", numA != numB)
print(str(numA) + "가 " + str(numB) + "보다 큰가?", numA > numB)
print(str(numA) + "가 " + str(numB) + "보다 작은가?", numA < numB)
