int triangleStar() {
    int n = 0, m = 0;

    printf("n, m 형식으로 입력해라 => ");
    scanf("%d, %d", &n, &m);

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n - i; j++) printf("  ");
        for (int j = 0; j < i + m + 1; j++) printf("%d ", j);

        printf("\n");
    }

    return 0;
}
