// Function Definitions
int getFactorial() {
    int till = 0, factorial = 1;

    printf("Enter a number: "); scanf("%d", &till);

    for (int i = 1; i <= till; i++) factorial *= i;
    printf("Factorial of %d is %d\n", till, factorial);

    return 0;
}

