int timeCheck() {
    int h, m, sum;

    printf("HH:MM형식으로 시간을 입력해라 => ");
    scanf("%02d:%02d", &h, &m);

    if (h > 23 || h < 0 || m < 0 || m > 59) {
        printf("넌 입력을 왜 그따구로 하니?\n");
        return 1;
    }

    sum = (h + m);
    
    if (h > 12 || h == 12) {
        if (sum % 7 == 0) printf("[가능] 돌아갈 수 있따!\n");
        else printf("[불가] 못 돌아간다.\n");
    } else if (h > 0 && h < 12) {
        if (sum % 3 == 0) printf("[가능] 돌아갈 수 있따!\n");
        else printf("[불가] 못 돌아간다.\n");
    } else {
        printf("출력될 수 없는 문장\n");
    }

    return 0;
}

